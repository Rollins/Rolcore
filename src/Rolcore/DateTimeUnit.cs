namespace Rolcore
{
    /// <summary>
    /// A unit of measurement for dates and times.
    /// </summary>
    public enum DateTimeUnit
    {
        /// <summary>
        /// One year of time.
        /// </summary>
        Year,
        /// <summary>
        /// One month of time.
        /// </summary>
        Month,
        /// <summary>
        /// One week of time.
        /// </summary>
        Week,
        /// <summary>
        /// One day of time.
        /// </summary>
        Day,
        /// <summary>
        /// One hour of time.
        /// </summary>
        Hour,
        /// <summary>
        /// One minute of time.
        /// </summary>
        Minute,
        /// <summary>
        /// One second of time.
        /// </summary>
        Second,
        /// <summary>
        /// One millisecond of time.
        /// </summary>
        Millisecond
    }
}
